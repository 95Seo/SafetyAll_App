package model;

public class Gyro {
    private String chAccX;
    private String chAccY;
    private String chAccZ;
    private String chGyX;
    private String chGyY;
    private String chGyZ;

    public String getChAccX() {
        return chAccX;
    }

    public void setChAccX(String chAccX) {
        this.chAccX = chAccX;
    }

    public String getChAccY() {
        return chAccY;
    }

    public void setChAccY(String chAccY) {
        this.chAccY = chAccY;
    }

    public String getChAccZ() {
        return chAccZ;
    }

    public void setChAccZ(String chAccZ) {
        this.chAccZ = chAccZ;
    }

    public String getChGyX() {
        return chGyX;
    }

    public void setChGyX(String chGyX) {
        this.chGyX = chGyX;
    }

    public String getChGyY() {
        return chGyY;
    }

    public void setChGyY(String chGyY) {
        this.chGyY = chGyY;
    }

    public String getChGyZ() {
        return chGyZ;
    }

    public void setChGyZ(String chGyZ) {
        this.chGyZ = chGyZ;
    }
}
